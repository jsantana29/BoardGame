        if(playerMines > 0){
            Integer playerPosition = p1.getPosition();
            
            for(int i = 0; i < p1MinePlaced.size();i++){
                if(playerPosition == p1MinePlaced.get(i)){
                    isMine = true;
                }
            }
            
            if(isMine){
                JOptionPane.showMessageDialog(null, "That's overkill hacker! You already have a mine there!");
            }
            else{
                p1MinePlaced.add(playerPosition);
            cmbP1MinesPlaced.addItem(playerPosition);
        
            index1++;
        
            playerMines--;
            p1.setMines(playerMines);
            lblp1Mines.setText(playerMines.toString());
        
            JOptionPane.showMessageDialog(null, "Player 1 has placed a mine on position "+playerPosition);
            }
            
        }
        else{
            JOptionPane.showMessageDialog(null, "You have no mines!");
        }
        
        btnP1Mines.setEnabled(false);
        btnP1Rifle.setEnabled(false);
        btnP1Missiles.setEnabled(false);
        btnP1Dewitos.setEnabled(false);
        
        
