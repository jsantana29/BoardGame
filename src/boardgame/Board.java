    private void btnRoll1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRoll1ActionPerformed
        // TODO add your handling code here:
        
        int playerRoll = p1.Player1Roll();
        boolean moveAgain = false;
        Integer playerPosition = p1.getPosition();
        
        lblPosition1.setText(playerPosition.toString());
        p1.Player1Moved(playerRoll);
        
        for(int i = 0; i < p2MinePlaced.size(); i++){
            if(playerPosition == p2MinePlaced.get(i)){
                JOptionPane.showMessageDialog(null, "Oh no! You got rekt by an enemy mine! You took 5 damage!");
                Integer p1Health = p1.isEnemyMine();
                lblp1Health.setText(p1Health.toString());
                cmbP2MinesPlaced.removeItem(p2MinePlaced.get(i));
                p2MinePlaced.remove(p2MinePlaced.get(i));
            }
        }
        
        for(int i = 0; i < shelter.size(); i++){
            if(playerPosition == shelter.get(i)){
                p1.isShelter();
                
            }  
        }
        
        for(int i = 0; i < tides.size(); i++){
            if(playerPosition == tides.get(i)){
                Integer chance = p1.isTides();
                
                if(chance == 1){
                    JOptionPane.showMessageDialog(null, "The tides have changed: Trip and lose 2 health!");
                    Integer p1Health = p1.getHealth();
                    p1Health -= 2;
                    p1.setHealth(p1Health);
                    lblp1Health.setText(p1Health.toString());
                }
                else if(chance == 2){
                    JOptionPane.showMessageDialog(null, "The tides have changed: Misfire and lose 1 rifle shot!");
                    Integer p1Shots = p1.getShots();
                    if(p1Shots > 0){
                        p1Shots--;
                        p1.setShots(p1Shots);
                        lblp1Rifles.setText(p1Shots.toString());
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "No rifle shots? What a shame!");
                    }
                    
                }
                else if(chance == 3){
                    JOptionPane.showMessageDialog(null, "The tides have changed: You accidently set off your own mine and throw it before it hurts you, losing 1 mine!");
                    Integer p1Mines = p1.getMines();
                    if(p1Mines > 0){
                        p1Mines--;
                        p1.setMines(p1Mines);
                        lblp1Mines.setText(p1Mines.toString());
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "No mines? Oh well!");
                    }
                   
                }
                else if(chance == 4){
                    JOptionPane.showMessageDialog(null, "The tides have changed: You found a bag of doritos and ate it for 2 health!");
                    Integer p1Health = p1.isSmallHealth();
                    lblp1Health.setText(p1Health.toString());
                }
                else if(chance == 5){
                    JOptionPane.showMessageDialog(null, "The tides have changed: You are getting excited and have the option to move again!");
                    moveAgain = true;
                }
                else if(chance == 6){
                    JOptionPane.showMessageDialog(null, "The tides have changed: Jackpot! You found a cliche treasure chest with $1000!");
                    Integer p1Money = p1.getMoney();
                    p1Money += 1000;
                    p1.setMoney(p1Money);
                    lblp1Money.setText(p1Money.toString());
                }
            }
        }
        
        for(int i = 0; i < health.size(); i++){
            if(playerPosition == health.get(i)){
                JOptionPane.showMessageDialog(null, "You made it to a medical center! You have been given +5 health!");
                Integer p1Health = p1.isHealth();
                lblp1Health.setText(p1Health.toString());
            }
        }
        
        for(int i = 0; i < moneyGain.size(); i++){
            if(playerPosition == moneyGain.get(i)){
                JOptionPane.showMessageDialog(null, "You found an abandoned wallet! Lucky you! You gain $400!");
                Integer p1Money = p1.isMoneyGain();
                lblp1Money.setText(p1Money.toString());
            }
        }
        
        for(int i = 0; i < moneyLost.size(); i++){
            if(playerPosition == moneyLost.get(i)){
                JOptionPane.showMessageDialog(null, "Oh no! You just got jacked by a filthy casual and lost $100!");
                Integer p1Money = p1.isMoneyLost();
                lblp1Money.setText(p1Money.toString());
            }
        }
        Integer p1Health = p1.getHealth();
        Integer p1Money = p1.getMoney();
                
        lblp1Health.setText(p1Health.toString());
        lblp1Money.setText(p1Money.toString());
        
        p1.isDead();
        p2.isDead();
        
        if(!moveAgain){
            btnRoll1.setEnabled(false);
        }
        
    }//GEN-LAST:event_btnRoll1ActionPerformed

